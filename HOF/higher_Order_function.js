// 1. 다른 함수를 인자로 받는 함수
function higherOrderFunction(func) {
    return function() {
        console.log('Before calling the function');
        func();
        console.log('After calling the function');
    }
}

function sayHello() {
    console.log('Hello, World!');
}

const wrappedSayHello = higherOrderFunction(sayHello);
wrappedSayHello();   //Before calling the function, Hello, World!, After calling the function
//=============================================================================================

function highOrder(a, b, callback) {
    const result = callback(a, b);
    return result;
}

function add(a, b) { 
    return a + b;
}

console.log(highOrder(5, 3, add));   //8

// 2. 함수를 반환하는 함수
function createMultiplier(multiplier) {
    return function(x) {
        return x * multiplier;
    }
}

const double = createMultiplier(2);
const triple = createMultiplier(3);

console.log(double(5));   //10
console.log(triple(5));   //15

//=============================================================================================

function add2(x) {
    return function (y) {
        return x + y;
    };
}
const addBy5 = add2(5); //함수를 반환하는 함수, addBy5는 add2 함수가 반환하는 함수, addBy5(3)은 add2 함수가 반환하는 함수에 3을 인자로 전달하여 실행, 결과는 5 + 3 = 8
console.log(addBy5(3));   //8